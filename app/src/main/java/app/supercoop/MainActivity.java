public class MainActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        FirebaseAuth auth = FirebaseAuth.getInstance();

        if(auth.getCurrentUser() != null) {
            Intent deviceManager = new Intent(this, DeviceManager.class);
            startActivity(deviceManager);
        }
    }

    public void newUserActivity(View view) {
        Intent newUser = new Intent(this, NewUser.class);
        startActivity(newUser);
    }

    public void existingUserActivity(View view) {
        Intent existingUser = new Intent(this, ExistingUser.class);
        startActivity(existingUser);
    }
}
